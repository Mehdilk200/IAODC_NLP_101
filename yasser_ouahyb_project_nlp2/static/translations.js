// Language Application Logic
function applyTranslations(lang) {
    document.documentElement.lang = lang;
    if (lang === 'ar') {
        document.documentElement.dir = 'rtl';
    } else {
        document.documentElement.dir = 'ltr';
    }

    const dict = translations[lang];
    if (!dict) return;

    // Translate all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            // Check if it's an input placeholder
            if (el.tagName.toLowerCase() === 'input' && el.hasAttribute('placeholder')) {
                el.placeholder = dict[key];
            } else {
                // If the element has children (like icons), we only want to replace the text nodes
                // To be safe and preserve icons, we use a simple approach: if it has an icon, it's usually the first child
                const icon = el.querySelector('i');
                if (icon) {
                    el.innerHTML = '';
                    el.appendChild(icon);
                    el.appendChild(document.createTextNode(' ' + dict[key]));
                } else {
                    el.textContent = dict[key];
                }
            }
        }
    });

    // Save preference
    localStorage.setItem('cmc_lang', lang);

    // Update active button state
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.dataset.lang === lang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
}

// Auto-init language
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('cmc_lang') || 'fr';
    applyTranslations(savedLang);

    // Bind language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', (e) => {
            const lang = e.currentTarget.dataset.lang;
            applyTranslations(lang);

            // Dispatch a custom event so that other scripts (like catalog) can refresh their content
            window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: lang } }));
        });
    });
});
